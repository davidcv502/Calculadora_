    public static boolean p(String cadena){
        boolean resultado;
        resultado = false;
        for (int i = 0; i < cadena.length(); i++){
            if (cadena.substring(i, i+1).equals(".")){
                resultado = true;
                break;
            }
            
        }
        return resultado;
    }
    
    public static String Op(String n1, String Signo, String n2){
        Double Total = 0.0;
        String Resultado;
        
        if(Signo.equals("/")){
            Total = Double.parseDouble(n1)/Double.parseDouble(n2);         
        } 
        if(Signo.equals("*")){
            Total = Double.parseDouble(n1)*Double.parseDouble(n2);         
        } 
        if(Signo.equals("+")){
            Total = Double.parseDouble(n1)+Double.parseDouble(n2);         
        } 
        if(Signo.equals("-")){
            Total = Double.parseDouble(n1)-Double.parseDouble(n2);         
        } 
        Resultado = Total.toString();
        return Resultado;
    }
